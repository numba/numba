_pid = None

def random_init():
    """
    Initialize the random states with system entropy.
    """
    global _pid
    if _pid != os.getpid():
        b = os.urandom(N * 4)
        for n in ('py_random_state', 'np_random_state'):
            _helperlib.rnd_seed(_helperlib.c_helpers[n], b)
        _pid = os.getpid()


